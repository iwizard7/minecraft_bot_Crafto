from PIL import Image, ImageDraw
import os

def create_crafto_texture():
    # –°–æ–∑–¥–∞–µ–º –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ 64x64 –ø–∏–∫—Å–µ–ª—è (—Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–π —Ä–∞–∑–º–µ—Ä —Å–∫–∏–Ω–∞ Minecraft)
    img = Image.new('RGBA', (64, 64), (0, 0, 0, 0))
    
    # –û—Å–Ω–æ–≤–Ω—ã–µ —Ü–≤–µ—Ç–∞ –¥–ª—è AI –±–æ—Ç–∞
    skin_color = (220, 200, 180)  # –°–≤–µ—Ç–ª—ã–π —Ç–æ–Ω –∫–æ–∂–∏
    shirt_color = (50, 100, 200)  # –°–∏–Ω–∏–π —Ü–≤–µ—Ç –¥–ª—è AI —Ç–µ–º–∞—Ç–∏–∫–∏
    pants_color = (30, 30, 50)    # –¢–µ–º–Ω–æ-—Å–∏–Ω–∏–π
    hair_color = (100, 100, 100)  # –°–µ—Ä—ã–π –¥–ª—è "—Ç–µ—Ö–Ω–æ–ª–æ–≥–∏—á–Ω–æ—Å—Ç–∏"
    
    draw = ImageDraw.Draw(img)
    
    # –ì–æ–ª–æ–≤–∞ (–ø–µ—Ä–µ–¥–Ω—è—è —á–∞—Å—Ç—å) - 8x8 –ø–∏–∫—Å–µ–ª–µ–π, –Ω–∞—á–∏–Ω–∞—è —Å (8, 8)
    draw.rectangle([8, 8, 15, 15], fill=skin_color)
    
    # –í–æ–ª–æ—Å—ã/—à–ª–µ–º - 8x8 –ø–∏–∫—Å–µ–ª–µ–π, –Ω–∞—á–∏–Ω–∞—è —Å (8, 8)
    draw.rectangle([8, 8, 15, 11], fill=hair_color)
    
    # –ì–ª–∞–∑–∞ - 2 –ø–∏–∫—Å–µ–ª—è
    draw.point((10, 12), fill=(0, 100, 255))  # –°–∏–Ω–∏–µ –≥–ª–∞–∑–∞ –¥–ª—è AI
    draw.point((13, 12), fill=(0, 100, 255))
    
    # –¢–µ–ª–æ (–ø–µ—Ä–µ–¥–Ω—è—è —á–∞—Å—Ç—å) - 8x12 –ø–∏–∫—Å–µ–ª–µ–π, –Ω–∞—á–∏–Ω–∞—è —Å (20, 20)
    draw.rectangle([20, 20, 27, 31], fill=shirt_color)
    
    # –†—É–∫–∏ (–ø–µ—Ä–µ–¥–Ω—è—è —á–∞—Å—Ç—å) - –ø–æ 4x12 –ø–∏–∫—Å–µ–ª–µ–π
    # –ü—Ä–∞–≤–∞—è —Ä—É–∫–∞
    draw.rectangle([44, 20, 47, 31], fill=skin_color)
    draw.rectangle([44, 20, 47, 23], fill=shirt_color)  # –†—É–∫–∞–≤
    
    # –õ–µ–≤–∞—è —Ä—É–∫–∞  
    draw.rectangle([36, 52, 39, 63], fill=skin_color)
    draw.rectangle([36, 52, 39, 55], fill=shirt_color)  # –†—É–∫–∞–≤
    
    # –ù–æ–≥–∏ (–ø–µ—Ä–µ–¥–Ω—è—è —á–∞—Å—Ç—å) - –ø–æ 4x12 –ø–∏–∫—Å–µ–ª–µ–π
    # –ü—Ä–∞–≤–∞—è –Ω–æ–≥–∞
    draw.rectangle([4, 20, 7, 31], fill=pants_color)
    
    # –õ–µ–≤–∞—è –Ω–æ–≥–∞
    draw.rectangle([20, 52, 23, 63], fill=pants_color)
    
    # –î–æ–±–∞–≤–ª—è–µ–º –ø—Ä–æ—Å—Ç—ã–µ –¥–µ—Ç–∞–ª–∏ –¥–ª—è "—Ç–µ—Ö–Ω–æ–ª–æ–≥–∏—á–Ω–æ—Å—Ç–∏"
    # –°–≤–µ—Ç—è—â–∏–µ—Å—è —Ç–æ—á–∫–∏ –Ω–∞ –≥—Ä—É–¥–∏ (–∫–∞–∫ –∏–Ω–¥–∏–∫–∞—Ç–æ—Ä—ã)
    draw.point((22, 22), fill=(0, 255, 255))  # Cyan —Ç–æ—á–∫–∞
    draw.point((25, 22), fill=(0, 255, 255))
    
    return img

