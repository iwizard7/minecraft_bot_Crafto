    print("  - –°–µ—Ä—ã–µ –≤–æ–ª–æ—Å—ã –¥–ª—è —Ç–µ—Ö–Ω–æ–ª–æ–≥–∏—á–Ω–æ—Å—Ç–∏")
    print("  - –°–∏–Ω–∏–µ –≥–ª–∞–∑–∞")
    print("  - –°–≤–µ—Ç—è—â–∏–µ—Å—è –∏–Ω–¥–∏–∫–∞—Ç–æ—Ä—ã –Ω–∞ –≥—Ä—É–¥–∏")

